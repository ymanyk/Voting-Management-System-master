    public ArrayList<Teacher> teacherlist() {
        ArrayList<Teacher> teacherlist = new ArrayList<>();
        try {

            Connection con = DriverManager.getConnection("jdbc:postgresql://localhost:5432/postgres", "postgres", "123");
            String sql = "select * from teacherlist";
            Statement ps = con.createStatement();

            ResultSet rs = ps.executeQuery(sql);
            Teacher user2;
            while (rs.next()) {
                user2 = new Teacher(rs.getString("teacher_id"), rs.getString("teacher_name"), rs.getString("faculty"), rs.getString("course"),rs.getString("password"));
                teacherlist.add(user2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(addTeacher.class.getName()).log(Level.SEVERE, null, ex);
        }
        return teacherlist;
    }

    public void showuser3() {
        ArrayList<Teacher> list = teacherlist();
        DefaultTableModel model = (DefaultTableModel) jcondidatedisplay.getModel();

        for (Teacher teacher : list) {
            Object[] row = new Object[4];  // Create a new array for each row
            row[0] = teacher.getTeacherid();
            row[1] = teacher.getTeacher_name();
            row[2] = teacher.getFaculty();
            row[3] = teacher.getCourse();

            model.addRow(row); // Add the new row array to the table model
        }

    }
    

